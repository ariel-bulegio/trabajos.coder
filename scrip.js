let ingresar = prompt ("Escriba algo")

while(ingresar != "ESC"){ 
    switch (ingresar){
        case "Buenos Dias":
            alert ("Hola Buenos Dias");
            break ;
            case "Como Estas":
                alert ("Hola Como Estas");
                break;
                default:
                    alert ("Quien sos")
                    break;

    }
ingresar = prompt ("Escriba algo")
}